  			</div>
  			<p>
  				Dans le premier chapitre de la première partie intitulée : <em>pourquoi de petites améliorations mènent à de grands résultats</em>, James Clear mentionne l'entraîneur de l'ex-équipe cycliste britannique Sky, Dave Brailsford, qui fonctionne selon le principe d'accumulation de petites améliorations à la base jugées insignifiantes. Il divise chaque étape d'entraînement d'un cycliste puis les améliore une par une afin d'avoir une grosse plus value finale.<br><br>

  				<em>"Trop souvent, nous nous convainquons nous-mêmes qu'un grand succès provient d'une grande action."</em> La dernière partie de la phrase est effectivement fausse car on ne peut pas s'améliorer instantanemment du jour au lendemain, cette étape d'amélioration requiert un temps et une persévérance. La théorie du <em>1% better every day</em> nous dit qu'en s'améliorant d'1% par jour on est 37% plus performant au bout d'un an, alors que sans progression on régresse à 0.<br><br>
